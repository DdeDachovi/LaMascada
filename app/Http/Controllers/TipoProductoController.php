<?php

namespace App\Http\Controllers;

use App\tipo_producto;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;

class TipoProductoController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categorias = tipo_producto::orderBy('id','ASC')->paginate();
        return view('Categorias.index',compact('categorias'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('Categorias.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
                'categoria' =>'required|string',
            ]
        );
        DB::insert('INSERT INTO tipo_productos(Tipo_producto) VALUES (?)',[$request->categoria]);
        return redirect()->route('categorias.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $c = tipo_producto::find($id);
        return view('Categorias.create',compact('c'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
                'categoria' =>'required|string',
            ]
        );
        DB::update('UPDATE tipo_productos SET Tipo_producto = ? WHERE id = ?',[$request->categoria,$id]);
        return redirect()->route('categorias.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        DB::delete('DELETE FROM tipo_productos WHERE id = ?',[$id]);
        return redirect()->route('categorias.index');
    }
}
